#include "libft.h"

char	*ft_itoa(int n)
{
	int		i;
	int		t;
	char	*s;

	if (n == -2147483648)
		return (ft_strdup("-2147483648"));
	i = 0;
	while (i++ > -1 && n < 0)
		n *= -1;
	t = n;
	while (t /= 10)
		i++;
	s = ft_strnew(i);
	if (!s)
		return (NULL);
	s[i] = '\0';
	s[--i] = n % 10 + 48;
	while (n /= 10)
		s[--i] = n % 10 + 48;
	if (i > 0)
		s[0] = '-';
	return (s);
}
